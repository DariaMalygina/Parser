    private List<UnitScript> scriptsList = new ArrayList<>();

    public List<UnitScript> getScriptAllUnits(String crudeScript) {
        parseString(crudeScript);
        System.out.println(scriptsList);
        return scriptsList;
    }

    private void parseString(String crudeScript) {
        Pattern p = Pattern.compile("(\\)[ \n]*\\()|([ \n]*[\\(\\)][ \n]*)");
        String[] scripts = p.split(crudeScript);

        for (String s : scripts) {
            if (!s.equals("")) {
                String clearString = s.replace("\n", "");
                clearString = clearString.replace("\t", "");
                clearString = clearString.replace(" ", "");
                System.out.println("clear string: " + clearString);
                UnitScript script = betweenComma(clearString);
                if (script != null){
                    scriptsList.add(script);
                }
            }
        }
    }

    private UnitScript betweenComma(String str) {
        Pattern p = Pattern.compile("(\\,\\{\\{)|(\\}\\})");
        String[] scripts = p.split(str);

        System.out.println("betweenComma: " + scripts.length);

        if (scripts.length == 2) {
            UnitScript script = speedWithUnit(scripts[0]);
            if (script != null) {
                coords(scripts[1], script);
                return script;
            }
        }
        return null;
    }

    private UnitScript speedWithUnit(String str) {
        Pattern p = Pattern.compile("\\,");
        String[] scripts = p.split(str);
        System.out.println("speedWithUnit: " + scripts.length);
        if (scripts.length != 2||scripts[0].equals("")||scripts[1].equals("")) {
            return null;
        }
        return new UnitScript(Integer.parseInt(scripts[0]),Float.parseFloat(scripts[1]));
    }

    private void coords(String str, UnitScript script) {
        Pattern p = Pattern.compile("\\}\\,\\{");
        String[] scripts = p.split(str);
        System.out.println("coords: " + scripts.length);
        for (String s : scripts) {
            if (!s.equals("")) {
                script.appendPosition(clearCoords(s));
            }
        }
    }

    private UnitPosition clearCoords(String str) {
        Pattern p = Pattern.compile("\\,");
        String[] scripts = p.split(str);
        System.out.println("clearCoords: " + scripts.length);

        double x = 0;
        double y = 0;
        int status = 0;
        int delay = 0;

        for (String s : scripts) {
            String[] varAndValue = s.split("=");
            switch (varAndValue[0]) {
                case "x":
                    x = Double.parseDouble(varAndValue[1]);
                    break;
                case "y":
                    y = Double.parseDouble(varAndValue[1]);
                    break;
                case "status":
                    status = Integer.parseInt(varAndValue[1]);
                    break;
                case "delay":
                    delay = Integer.parseInt(varAndValue[1]);
                    break;
                default:
                    break;
            }
        }
        return new UnitPosition(x,y,status,delay);
    }

